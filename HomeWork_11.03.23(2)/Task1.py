n = input('Введите день порядковый номер дня от 1 до 7: ')
if n == '1':
    print('Понедельник')
elif n == '2':
    print('Вторник')
elif n == '3':
    print('Среда')
elif n == '4':
    print('Четверг')
elif n == '5':
    print('Пятница')
elif n == '6':
    print('Суббота')
elif n == '7':
    print('Воскресенье')
else:
    print("Введите число от 1 до 7")